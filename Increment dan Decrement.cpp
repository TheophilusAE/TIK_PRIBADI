#include <iostream>

using namespace std;

int main () {
// Increment and Decrement

  int a = 1;
  int b = 2;
  int c = 3;
  int d = 4;

// increment 

// preincrement (++variabel)
  cout << a << endl;
  ++a; 
  cout << a << endl << endl;
  
// postincrement (Variabel++)
  cout << b << endl;
  b++;
  cout << b << endl << endl;
  
// predecrement (--Variabel)
  cout << c << endl;
  --c;
  cout << c << endl << endl;
  
// postdecrement (Variabel--)
  cout << d << endl;
  d--;
  cout << d << endl << endl;
  
  return 0;
}
